package br.com.kumabe.helloworld

class ArithmeticOperation {

    fun sum(numberA: Double, numberB: Double): Double {
        return numberA + numberB
    }

    fun sub(numberA: Double, numberB: Double): Double {
        return numberA - numberB
    }

    fun mult(numberA: Double, numberB: Double): Double {
        return numberA * numberB
    }

    fun div(numberA: Double, numberB: Double): Double {
        return numberA / numberB
    }

}